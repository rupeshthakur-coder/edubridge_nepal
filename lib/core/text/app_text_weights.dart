import 'package:flutter/material.dart';

class TextFontWeights {
  // Extra Bold for headings or titles
  static const FontWeight extraBold = FontWeight.w800;

  // Bold for subheadings or section titles
  static const FontWeight bold = FontWeight.w700;

  // Semi-Bold for emphasized text like buttons or highlighted labels
  static const FontWeight semiBold = FontWeight.w600;

  // Medium for body text with slight emphasis
  static const FontWeight medium = FontWeight.w500;

  // Regular for standard body text
  static const FontWeight regular = FontWeight.w400;

  // Light for secondary text, captions, or labels
  static const FontWeight light = FontWeight.w300;

  // Thin for less prominent elements like hints or minor details
  static const FontWeight thin = FontWeight.w200;
}
